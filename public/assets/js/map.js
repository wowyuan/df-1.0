function lodemarker(){
    var map = new BMap.Map("map");
    var point = new BMap.Point(121.529327, 31.0959);
    map.centerAndZoom(point, 17);
    var marker = new BMap.Marker(new BMap.Point(121.529327, 31.0959));
    map.addOverlay(marker);    
}
